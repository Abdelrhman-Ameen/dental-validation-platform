import { execFileSync } from "node:child_process";
import { existsSync, mkdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { createInterface } from "node:readline/promises";
import { stdin as input, stdout as output } from "node:process";

const root = process.cwd();
const npx = process.platform === "win32" ? "npx.cmd" : "npx";

function argValue(name) {
  const index = process.argv.indexOf(name);
  return index === -1 ? "" : process.argv[index + 1] || "";
}

function runFirebase(args, { json = false } = {}) {
  const fullArgs = ["firebase-tools", ...args];
  const stdout = execFileSync(npx, fullArgs, {
    cwd: root,
    encoding: "utf8",
    stdio: json ? ["ignore", "pipe", "pipe"] : "inherit"
  });

  if (!json) {
    return null;
  }

  try {
    return JSON.parse(stdout);
  } catch {
    throw new Error(`Firebase CLI did not return JSON for: firebase ${args.join(" ")}`);
  }
}

async function promptProjectId() {
  const envProject =
    process.env.FIREBASE_PROJECT_ID ||
    process.env.NEXT_PUBLIC_FIREBASE_PROJECT_ID ||
    argValue("--project") ||
    argValue("-P");

  if (envProject) {
    return envProject;
  }

  const rl = createInterface({ input, output });
  const value = await rl.question("Firebase project id: ");
  rl.close();

  if (!value.trim()) {
    throw new Error("A Firebase project id is required.");
  }
  return value.trim();
}

function unwrapResult(payload) {
  return payload?.result ?? payload;
}

function normalizeApp(app) {
  return {
    appId: app.appId || app.app_id || app.appID || app.name?.split("/").pop(),
    displayName: app.displayName || app.display_name || app.name || ""
  };
}

function envLine(key, value) {
  return `${key}=${value ?? ""}`;
}

async function main() {
  const projectId = await promptProjectId();

  console.log(`Using Firebase project: ${projectId}`);
  console.log("Checking Firebase login...");
  runFirebase(["projects:list", "--json"], { json: true });

  console.log("Looking for an existing Web App...");
  const listPayload = runFirebase(["apps:list", "WEB", "--project", projectId, "--json"], { json: true });
  const appsResult = unwrapResult(listPayload);
  const apps = Array.isArray(appsResult) ? appsResult.map(normalizeApp) : [];

  let app = apps.find((candidate) => candidate.displayName.includes("Dental Validation")) || apps[0];

  if (!app?.appId) {
    console.log("No Web App found. Creating one...");
    const createPayload = runFirebase(
      ["apps:create", "WEB", "Dental Validation Platform", "--project", projectId, "--json"],
      { json: true }
    );
    app = normalizeApp(unwrapResult(createPayload));
  }

  if (!app?.appId) {
    throw new Error("Could not resolve a Firebase Web App id.");
  }

  console.log(`Using Web App: ${app.appId}`);
  const configPayload = runFirebase(["apps:sdkconfig", "WEB", app.appId, "--project", projectId, "--json"], {
    json: true
  });
  const configResult = unwrapResult(configPayload);
  const sdkConfig = configResult.sdkConfig || configResult;

  if (!sdkConfig.apiKey || !sdkConfig.projectId) {
    throw new Error("Firebase SDK config was incomplete. Check that the Web App exists.");
  }

  const envPath = join(root, "frontend", ".env.local");
  const envContent = [
    "# Generated by npm run firebase:setup",
    envLine("NEXT_PUBLIC_FIREBASE_API_KEY", sdkConfig.apiKey),
    envLine("NEXT_PUBLIC_FIREBASE_AUTH_DOMAIN", sdkConfig.authDomain),
    envLine("NEXT_PUBLIC_FIREBASE_PROJECT_ID", sdkConfig.projectId),
    envLine("NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID", sdkConfig.messagingSenderId),
    envLine("NEXT_PUBLIC_FIREBASE_APP_ID", sdkConfig.appId),
    envLine("NEXT_PUBLIC_FIREBASE_MEASUREMENT_ID", sdkConfig.measurementId || ""),
    envLine("NEXT_PUBLIC_FIREBASE_FUNCTIONS_REGION", "us-central1"),
    envLine("NEXT_PUBLIC_APP_URL", "http://localhost:3000"),
    "",
    "# Firebase Admin SDK values are needed for Next.js server routes on Vercel.",
    "# Use a service account key from Firebase Console > Project settings > Service accounts.",
    envLine("FIREBASE_PROJECT_ID", sdkConfig.projectId),
    envLine("FIREBASE_CLIENT_EMAIL", ""),
    envLine("FIREBASE_PRIVATE_KEY", ""),
    ""
  ].join("\n");

  writeFileSync(envPath, envContent, "utf8");
  writeFileSync(
    join(root, ".firebaserc"),
    `${JSON.stringify({ projects: { default: projectId } }, null, 2)}\n`,
    "utf8"
  );

  const firebaseDir = join(root, ".firebase");
  if (!existsSync(firebaseDir)) {
    mkdirSync(firebaseDir, { recursive: true });
  }
  writeFileSync(join(firebaseDir, "project-id.txt"), `${projectId}\n`, "utf8");

  console.log("Wrote frontend/.env.local");
  console.log("Wrote .firebaserc");
  console.log("");
  console.log("Next steps:");
  console.log("1. Enable Authentication > Sign-in method > Google in Firebase Console.");
  console.log("2. Add localhost and your deployed domain in Authentication > Settings > Authorized domains.");
  console.log("3. Run: npm run firebase:deploy");
  console.log("4. Run: npm run seed");
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exitCode = 1;
});
